<h1>Historische Luftbilder swisstopo</h1>

<p>Quelle: Bundesamt für Landestopografie swisstopo</p>

<p>Die Bildsammlung von swisstopo umfasst rund 500‘000 analoge Luftbilder (Farb-, Farb-Infrarot- oder SW-Bilder) und terrestrische Aufnahmen. Rund 320‘000 Aufnahmen sind in Form der Bildzentrumskoordinaten, des jeweiligen abgedeckten Bodensegments (Footprint) sowie mit Metainformationen dargestellt. Rund 160‘000 Bilder dieses Bestandes sind bereits digitalisiert und können in voller Auflösung betrachtet werden (Stand Januar 2014). Die noch ausstehenden Bilder und Scans werden in den kommenden Jahren laufend integriert. Die Luftbilder können als digitale Datei bestellt werden. Sofern vorhanden können zudem die Orientierungselemente zu den Luftbildern geliefert werden.</p>

