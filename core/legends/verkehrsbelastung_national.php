<h1>Belastung des schweizerischen Strassennetzes nach Fahrzeugkategorien</h1>

<p>Fahrzeuge auf dem schweizerischen Strassennetz unterschieden nach Personenwagen (PW), Lieferwagen (LI), Lastwagen (LW) und Lastzügen (LZ) im Jahr 2017. Tageswerte für den durchschnittlichen Werktagverkehr (DWV) und den durchschnittlichen Tagesverkehr (DTV) sowie Werte der Morgenspitze (7h-8h, MSP) und der Abendspitze (17h-18h, ASP). Quelle: Nationales Personenverkehrsmodell (NPVM) und Aggregierte Methode Güterverkehr (AMG) des UVEK.</p>

