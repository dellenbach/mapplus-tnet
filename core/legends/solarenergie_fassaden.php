<h1>Eignung von Hausfassaden für die Nutzung von Sonnenenergie</h1>

<p>Quelle: Bundesamt für Energie</p>

<p>Die Sonnenenergie, welche auf die Erdoberfläche trifft, kann durch die beiden bewährten Solartechnologien Photovoltaik (Strom) und Solarthermie (Wärme) genutzt werden, um selber erneuerbare Energie zu produzieren.Das Potenzial von Solarstrom ist beträchtlich: bis zum Jahr 2050 könnten rund 20 Prozent des derzeitigen Strombedarfs durch Photovoltaik erzeugt werden. Solarwärme ist dank standardisierten Anlagentypen für Warmwasser und Heizungsunterstützung eine interessante Option. Die Eignungskarte zeigt für eine Fassade, wie gut es für die Nutzung von Solarenergie geeignet ist und welcher Ertrag möglich wäre. Dafür wird der Sonnengang über das Jahr simuliert und die auf jede Fassaden treffende Sonnenstrahlung berechnet. Die angegebene Fläche beträgt die gesamte Fassadenfläche und beinhaltet keine Reduktion von Fenstern oder ähnlichem. Für diese Berechnungen wurden Strahlungsdaten des Bundesamtes für Meteorologie und Klimatologie MeteoSchweiz und 3D-Gebäudedaten des Bundesamtes für Landestopografie swisstopo (Produkt swissBUILDINGS3D 2.0) verwendet. Zurzeit bildet die Eignungskarte rund einen Drittel der Schweiz ab. Sobald die notwendigen Grundalgendaten vorliegen, wird die Eignungskarte ergänzt. Voraussichtlich Mitte 2019 ist die ganze Schweiz abgedeckt.</p>

