<h1>Eignung von Hausdächern für die Nutzung von Sonnenenergie</h1>

<p>Quelle: Bundesamt für Energie</p>

<p>Die Sonnenenergie, welche auf die Erdoberfläche trifft, kann durch die beiden bewährten Solartechnologien Photovoltaik (Strom) und Solarthermie (Wärme) genutzt werden, um selber erneuerbare Energie zu produzieren. Das Potenzial von Solarstrom ist beträchtlich: bis zum Jahr 2050 könnten rund 20 Prozent des derzeitigen Strombedarfs durch Photovoltaik erzeugt werden. Solarwärme ist dank standardisierten Anlagentypen für Warmwasser und Heizungsunterstützung eine interessante Option. Die Eignungskarte zeigt für ein Dach, wie gut es für die Nutzung von Solarenergie geeignet ist und welcher Ertrag möglich wäre. Dafür wird der Sonnengang über das Jahr simuliert und die auf jede Dachfläche treffende Sonnenstrahlung berechnet. Momentan sind 50 % des Gebäudebestandes erfasst; bis Anfang 2018 werden alle Hausdächer der Schweiz verfügbar sein.</p>

