    <h2>Digitales Höhenmodell (DHM)</h2>
    Die Höhenangaben des digitalen Terrainmodells mit 50cm Rasterweite bilden
    die <b>Erdoberfläche ohne Gebäude und Vegetation ab</b>. Die Daten werden mit
    Airborne Laser Scanning (LIDAR) erfasst. Die Karte zeigt das daraus abgeleitete Relief
    an. Die Höhen von DTM und DOM sowie Differenz können durch Klicken in der
    Karte abgefragt werden (minimal abweichende Differenzen durch Rundung möglich).

    <h2>Digitales Oberflächenmodell (DOM)</h2>
    Das digitale Oberflächenmodell beinhaltet hochpräzise Höhenangaben zu
    <b>Erdoberfläche, Gebäuden und Vegetation (Rasterweite 0.5m)</b>. Die Daten werden
    mit Airborne Laser Scanning (LIDAR) erfasst.
    
