<h2>Eidg. Gebäude- und Wohnungsregister: Energie-/Wärmequelle Heizung (Bundesamt für Statistik BFS)</h2>

<p>Das eidgenössische Gebäude- und Wohnungsregister (GWR) wurde anhand von Daten der Volkszählung 2000 zu statistischen Zwecken erstellt. Das GWR hat sich nicht zuletzt dank der am 1. Juli 2017 in Kraft getretenen Totalrevision der Verordnung (VGWR) als massgebendes nationales Informationssystem für Gebäude etabliert. Das GWR enthält Informationen zu Bauprojekten, Gebäuden, Wohnungen, Gebäudeeingängen und Strassen. Daten der Stufe A sind öffentlich und können ohne Einschränkung verwendet werden. Die Datenebene "GWR: Energie-/Wärmequelle der Heizung" veranschaulicht grafisch (Farbe) die Energie-/Wärmequelle des leistungsstärksten Heizsystems.</p>

<p><b>WICHTIG, zu beachten</b>: Die Daten sind nur teilweiuse aktualisert. Die Daten, die noch aus der Volkszählung 2000 stammen, sind schwarz umrandet.</p>

